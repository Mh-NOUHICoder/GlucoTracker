interface I18nContextType {
  lang: Language;
  setLang: (lang: Language) => void;
  t: (key: string) => string;
  dir: "ltr" | "rtl";
}

const I18nContext = createContext<I18nContextType>({
  lang: "en",
  setLang: () => {},
  t: (k) => k,
  dir: "ltr"
});

export const I18nProvider = ({ children }: { children: React.ReactNode }) => {
  const [lang, setLang] = useState<Language>("en");
  const [mounted, setMounted] = useState(false);

  // Persist language configuration locally
  useEffect(() => {
    const saved = localStorage.getItem("lang") as Language;
    if (saved && dict[saved]) setLang(saved);
    setMounted(true);
  }, []);

  const changeLang = (l: Language) => {
    setLang(l);
    localStorage.setItem("lang", l);
  };

  const t = (key: string) => {
    return dict[lang][key] || key;
  };

  const dir = lang === "ar" ? "rtl" : "ltr";

  // Prevent hydration mismatch by rendering cleanly
  if (!mounted) {
    return <div style={{ visibility: "hidden" }}>{children}</div>;
  }

  return (
    <div dir={dir} className={lang === "ar" ? "font-arabic" : ""}>
       <I18nContext.Provider value={{ lang, setLang: changeLang, t, dir }}>
         {children}
       </I18nContext.Provider>
    </div>
  );
};

export const useI18n = () => useContext(I18nContext);
